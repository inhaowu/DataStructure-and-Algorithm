#include<stdio.h>
#include<string.h>
char infilename[10]="0.in";
char outfilename[10]="0.out";
FILE * fin, * fout;
using namespace std;
const int maxn=401;
int edge[maxn][maxn];  
int color[maxn];      
int n;
bool dfs(int u,int c)
{
    color[u]=c;     //对u点进行染色
    for(int v=1;v<=n;v++)//遍历与u点相连的点
    {
        if(edge[u][v]==1)//如果i点与u点相连
        {
            if(color[v]==c) return false;//i点的染色重复，则不是二分图
            if(!color[v])
			   if(!dfs(v,-c)) return false;//该点未染色，染上相反的颜色.dfs继续搜索
        }
    }
    return true;  //所有点染色完成之后，并且相邻顶点没有同色，则为二分图
}
int main()
{
    for(int t=0;t<10;t++){
        infilename[0] = '0' + t;
		outfilename[0] = '0' + t;
		fout = fopen(outfilename, "w");
		fin = fopen(infilename, "r");
		printf("%d",t);
		int k;
		fscanf(fin,"%d",&k);
		for(int m=1;m<=k;m++){
			fscanf(fin,"%d",&n);
			memset(edge,0,sizeof(edge));
			memset(color,0,sizeof(color));
			for(int i=1;i<=n;i++){
				for(int j=1;j<=n;j++){
					fscanf(fin,"%d",&edge[i][j]);
				}
			}
			bool flag=false;
			for(int i=1;i<=n;i++){
				if(!color[i]){
					if(!dfs(i,1)){
						fprintf(fout,"No\n");
						flag=true;
						break;
					}
				}
			}
			if(!flag) fprintf(fout,"Yes\n");
		}
	}
	return 0;
}



